    public void cargarDatos(Datos datos, int idioma, String modelo, Icon icono){
        this.idi = idioma;
        this.dat = datos;
        this.imagen.setIcon(icono);
        this.modelo.setText(modelo);
        
        this.Titulo.setText(datos.getIdiomas().get(idioma).get(1));
        this.texto.setText(datos.getIdiomas().get(idioma).get(2));
        this.c1texto.setText(datos.getIdiomas().get(idioma).get(3));
        this.c2texto.setText(datos.getIdiomas().get(idioma).get(4));
        this.c3texto.setText(datos.getIdiomas().get(idioma).get(5));
        this.c4texto.setText(datos.getIdiomas().get(idioma).get(6));
        this.c5texto.setText(datos.getIdiomas().get(idioma).get(7));
        this.c5field.setText(datos.getIdiomas().get(idioma).get(8));
        this.c5field2.setText(datos.getIdiomas().get(idioma).get(9));
        
        int index = -1;
        for(int i = 0; i < datos.getReservas().size(); i++){
            if(modelo.contains(datos.getReservas().get(i).get(0))){
                index = i;
            }
        }
        
        for(int i = 1; i < datos.getReservas().get(index).size(); i++){
            this.c4field.addItem(datos.getReservas().get(index).get(i));
        }
    }

