    private void reservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reservarActionPerformed
        // TODO add your handling code here:
        PaginaMensaje paginaMensaje = new PaginaMensaje();
        paginaMensaje.setVisible(true);
        if(this.c1field.getText().equals("")){
            paginaMensaje.setTexto(this.dat.getIdiomas().get(idi).get(23));
        }else if(this.c2field.getText().equals("")){
            paginaMensaje.setTexto(this.dat.getIdiomas().get(idi).get(23));
        }else if(this.c3field.getText().equals("")){
            paginaMensaje.setTexto(this.dat.getIdiomas().get(idi).get(23));
        }else if(this.c4field.getSelectedItem().toString().equals("-")){
            paginaMensaje.setTexto(this.dat.getIdiomas().get(idi).get(23));
        }else if(this.c5field.isSelected() == false){
            paginaMensaje.setTexto(this.dat.getIdiomas().get(idi).get(23));
        }else if(this.c5field2.isSelected() == false){
            paginaMensaje.setTexto(this.dat.getIdiomas().get(idi).get(23));
        }else{
            paginaMensaje.setTexto(this.dat.getIdiomas().get(idi).get(22));
            
            int index = -1;
            for(int i = 0; i < dat.getReservas().size(); i++){
                if(this.modelo.getText().contains(dat.getReservas().get(i).get(0))){
                    index = i;
                }
            }
            
            for(int i = 0; i < dat.getReservas().get(index).size(); i++){
                if(dat.getReservas().get(index).get(i).contains(this.c4field.getSelectedItem().toString())){
                    dat.getReservas().get(index).remove(i);
                    break;
                }
            }
            
            this.dispose();
        }
        
        
        
        
        
    }//GEN-LAST:event_reservarActionPerformed

