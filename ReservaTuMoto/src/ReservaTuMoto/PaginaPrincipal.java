    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        boolean flag = true, flag1 = true, flag2 = true, flag3 = true;
        
        if(this.marca.getSelectedItem().toString().equals(this.datos.getIdiomas().get(idiomaActual).get(24))  &&
           this.modelo.getSelectedItem().toString().equals(this.datos.getIdiomas().get(idiomaActual).get(25))  &&
           this.fecha.getSelectedItem().toString().equals(this.datos.getIdiomas().get(idiomaActual).get(26))){
            
            this.card.setVisible(flag);
            this.card1.setVisible(flag1);
            this.card2.setVisible(flag2);
            this.card3.setVisible(flag3);
            return;
        }
        
        
        //comprobar marca
        if(!this.marca.getSelectedItem().toString().equals(this.datos.getIdiomas().get(idiomaActual).get(24))){
            if(!this.titulo1.getText().contains(this.marca.getSelectedItem().toString())){
                flag = false;
            }
            if(!this.titulo2.getText().contains(this.marca.getSelectedItem().toString())){
                flag1 = false;
            }
            if(!this.titulo3.getText().contains(this.marca.getSelectedItem().toString())){
                flag2 = false;
            }
            if(!this.titulo4.getText().contains(this.marca.getSelectedItem().toString())){
                flag3 = false;
            }
        }
        
        
        //comprobar modelo
        if(!this.modelo.getSelectedItem().toString().equals(this.datos.getIdiomas().get(idiomaActual).get(25))){
            if(!this.titulo1.getText().contains(this.modelo.getSelectedItem().toString())){
                flag = false;
            }
            if(!this.titulo2.getText().contains(this.modelo.getSelectedItem().toString())){
                flag1 = false;
            }
            if(!this.titulo3.getText().contains(this.modelo.getSelectedItem().toString())){
                flag2 = false;
            }
            if(!this.titulo4.getText().contains(this.modelo.getSelectedItem().toString())){
                flag3 = false;
            }
        }
        
        
        //comprobar fechas
        if(!this.fecha.getSelectedItem().toString().equals(this.datos.getIdiomas().get(idiomaActual).get(26))){
            //primera comprobacion
            boolean comprobacion = true;
            for(int i = 1; i < this.datos.getReservas().get(0).size(); i++){
                if(this.datos.getReservas().get(0).get(i).equals(this.fecha.getSelectedItem().toString())){
                    comprobacion = false;
                }
            }
            if(comprobacion){
                flag = false;
            }
            
            //segunda
            comprobacion = true;
            for(int i = 1; i < this.datos.getReservas().get(1).size(); i++){
                if(this.datos.getReservas().get(1).get(i).equals(this.fecha.getSelectedItem().toString())){
                    comprobacion = false;
                }
            }
            if(comprobacion){
                flag1 = false;
            }

            //tercera
            comprobacion = true;
            for(int i = 1; i < this.datos.getReservas().get(2).size(); i++){
                if(this.datos.getReservas().get(2).get(i).equals(this.fecha.getSelectedItem().toString())){
                    comprobacion = false;
                }
            }
            if(comprobacion){
                flag2 = false;
            }
            
            //cuarta
            comprobacion = true;
            for(int i = 1; i < this.datos.getReservas().get(3).size(); i++){
                if(this.datos.getReservas().get(3).get(i).equals(this.fecha.getSelectedItem().toString())){
                    comprobacion = false;
                }
            }
            if(comprobacion){
                flag3 = false;
            }
            
        }
        
        
        this.card.setVisible(flag);
        this.card1.setVisible(flag1);
        this.card2.setVisible(flag2);
        this.card3.setVisible(flag3);
        
    }//GEN-LAST:event_buscarActionPerformed

    private void comboIdiomaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboIdiomaActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String aux = (String) this.comboIdioma.getSelectedItem();
        if (aux.equals("Espa単ol")) {
            this.idiomaActual = 0;
        } else if (aux.equals("Ingles")) {
            this.idiomaActual = 1;
        }

        this.descripcion1.setText(datos.getIdiomas().get(idiomaActual).get(11));
        this.descripcion2.setText(datos.getIdiomas().get(idiomaActual).get(14));
        this.descripcion3.setText(datos.getIdiomas().get(idiomaActual).get(17));
        this.descripcion4.setText(datos.getIdiomas().get(idiomaActual).get(20));
        this.marca.removeAllItems();
        this.marca.addItem(datos.getIdiomas().get(this.idiomaActual).get(24));
        for (String marca1 : datos.getMarcas()) {
            this.marca.addItem(marca1);
        }

        this.modelo.removeAllItems();
        this.modelo.addItem(datos.getIdiomas().get(this.idiomaActual).get(25));
        for (String modelo1 : datos.getModelos()) {
            this.modelo.addItem(modelo1);
        }

        this.fecha.removeAllItems();
        this.fecha.addItem(datos.getIdiomas().get(this.idiomaActual).get(26));
        for (String fecha1 : datos.getFechas()) {
            this.fecha.addItem(fecha1);
        }
        
        //menu
        this.menuVentana.setText(datos.getIdiomas().get(idiomaActual).get(27));
        this.menuAcciones.setText(datos.getIdiomas().get(idiomaActual).get(28));
        this.ventana.setText(datos.getIdiomas().get(idiomaActual).get(29));
        this.tama単o1.setText(datos.getIdiomas().get(idiomaActual).get(30));
        this.tama単o2.setText(datos.getIdiomas().get(idiomaActual).get(31));
        this.colorFondo.setText(datos.getIdiomas().get(idiomaActual).get(32));
        this.color1.setText(datos.getIdiomas().get(idiomaActual).get(33));
        this.color2.setText(datos.getIdiomas().get(idiomaActual).get(34));
        this.descargar.setText(datos.getIdiomas().get(idiomaActual).get(35));
    }//GEN-LAST:event_comboIdiomaActionPerformed

